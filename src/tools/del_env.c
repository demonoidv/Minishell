#include "minishell.h"

void	del_env(char ****env)
{
	int		i;

	i = 0;
	while (env && *env && (*env)[i])
	{
		ft_strdel(&((*env)[i][0]));
		ft_strdel(&((*env)[i][1]));
		i++;
	}
	ft_memdel((void**)env);
}
